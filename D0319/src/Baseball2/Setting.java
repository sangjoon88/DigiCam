
package Baseball2;
import java.util.*;
public class Setting {
	
	int strike;
	int ball;
	int t;
	ArrayList<Integer>com;
	ArrayList<Integer>me;
	
	public int getStrike() {
		return strike;
	}
	public void setStrike(int strike) {
		this.strike = strike;
	}
	public int getBall() {
		return ball;
	}
	public void setBall(int ball) {
		this.ball = ball;
	}
	public int getT() {
		return t;
	}
	public void setT(int t) {
		this.t = t;
	}
	public ArrayList<Integer> getCom() {
		return com;
	}
	public void setCom(ArrayList<Integer> com) {
		this.com = com;
	}
	public ArrayList<Integer> getMe() {
		return me;
	}
	public void setMe(ArrayList<Integer> me) {
		this.me = me;
	}

	
	
}
